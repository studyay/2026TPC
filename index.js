function wrapTextKoreanSafe(ctx, text, maxWidth) {
  const lines = [];
  let line = "";

  const words = text.split(/\s+/).filter(Boolean);
  const useWordMode = words.length > 1; // ê³µë°±ì´ ê±°ì˜ ì—†ìœ¼ë©´ false
  const units = useWordMode ? words : Array.from(text);

  for (let i = 0; i < units.length; i++) {
    const u = units[i];
    const testLine = line
      ? (useWordMode ? `${line} ${u}` : `${line}${u}`)
      : u;

    if (ctx.measureText(testLine).width <= maxWidth) {
      line = testLine;
      continue;
    }

    // lineì´ ë¹„ì–´ ìˆëŠ”ë°ë„ í­ì„ ë„˜ëŠ” ê²½ìš°(ì•„ì£¼ ê¸´ í† í°) -> ê¸€ì ë‹¨ìœ„ë¡œ ê°•ì œ ë¶„í•´
    if (!line) {
      const chars = Array.from(u);
      let chunk = "";
      for (const ch of chars) {
        const t = chunk + ch;
        if (ctx.measureText(t).width > maxWidth && chunk) {
          lines.push(chunk);
          chunk = ch;
        } else {
          chunk = t;
        }
      }
      if (chunk) lines.push(chunk);
      line = "";
      continue;
    }

    lines.push(line);
    line = u;
  }

  if (line) lines.push(line);
  return lines;
}

